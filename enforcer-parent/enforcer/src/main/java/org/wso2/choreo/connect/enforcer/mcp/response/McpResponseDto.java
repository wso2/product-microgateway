package org.wso2.choreo.connect.enforcer.mcp.response;

/**
 * This class is used to represent a generic MCP response.
 */
public class McpResponseDto {
    private String response;
    private int statusCode;
    private String sessionId;

    public McpResponseDto(String response, int statusCode, String sessionId) {
        this.response = response;
        this.statusCode = statusCode;
        this.sessionId = sessionId;
    }

    public String getResponse() {
        return response;
    }

    public void setResponse(String response) {
        this.response = response;
    }

    public int getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(int statusCode) {
        this.statusCode = statusCode;
    }

    public String getSessionId() {
        return sessionId;
    }

    public void setSessionId(String sessionId) {
        this.sessionId = sessionId;
    }
}
